class Profit{
	public static void main(String [] args){
		int SP=191 ,CP=129;
		int profit=SP-CP;
		double profitPercentage=(double)(profit*100)/CP;
		System.out.println("The Cost Price is "+ CP + " INR and Selling Price is "+ SP+" INR");
		System.out.println("The Profit is "+ profit +" INR and the Profit Percentage is " +profitPercentage );
	}
}
